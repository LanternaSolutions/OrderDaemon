/**
 * Get Log Event Types Registry - Single Source of Truth for Known Events
 *
 * This function returns a comprehensive registry of all known log event types
 * that can be generated by the Order Daemon plugin. Each event type includes
 * metadata for consistent logging, dynamic summary generation, and proper
 * categorization within the new audit log architecture.
 *
 * REGISTRY STRUCTURE:
 * ==================
 * 
 * Each event type is defined with the following properties:
 * - id: Unique event slug used for registry lookup
 * - label: Human-readable title for admin interfaces
 * - summary_template: sprintf template for dynamic summary generation
 * - default_status: Default status slug for this event type
 * - category: Log category (core, debug, premium, custom)
 * 
 * SUMMARY TEMPLATES:
 * =================
 * 
 * Templates use sprintf placeholders that are populated from context data:
 * - %d: Integer values (order IDs, counts, etc.)
 * - %s: String values (names, messages, etc.)
 * - %f: Float values (amounts, percentages, etc.)
 * 
 * Example: "Order #%d completed successfully" + ['order_id' => 123]
 * Result: "Order #123 completed successfully"
 * 
 * CATEGORIZATION SYSTEM:
 * =====================
 * 
 * Events are categorized to enable proper filtering and UI treatment:
 * 
 * CORE EVENTS: Essential plugin functionality
 * - Always logged regardless of debug mode
 * - Visible to all users in audit trail
 * - Critical for troubleshooting and monitoring
 * 
 * DEBUG EVENTS: Developer-focused verbose logging
 * - Only logged when ODCM_DEBUG constant is true
 * - Detailed execution flow and state information
 * - Hidden from production audit trails
 * 
 * PREMIUM EVENTS: Advanced feature logging
 * - Always saved to database regardless of license
 * - Marked for special UI treatment (premium badges)
 * - Used for upgrade prompts and feature discovery
 * 
 * EXTENSIBILITY:
 * =============
 * 
 * New event types can be added by:
 * 1. Adding entry to this registry function
 * 2. Using odcm_log_event() with the new event slug
 * 3. Providing appropriate context data for summary template
 * 
 * The registry is designed to be easily extended without breaking
 * existing functionality or requiring database changes.
 *
 * @since 1.0.0
 *
 * @return array<string, array<string, mixed>> {
 *     Associative array of event types keyed by event slug.
 *     
 *     @type array $event_slug {
 *         Individual event type definition.
 *         
 *         @type string $id               Unique event identifier (matches array key).
 *         @type string $label            Human-readable event name for admin UI.
 *         @type string $summary_template sprintf template for dynamic summary generation.
 *         @type string $default_status   Default status slug for this event type.
 *         @type string $category         Log category: 'core', 'debug', 'premium', or 'custom'.
 *     }
 * }
 *
 * @example
 * ```php
 * // Get all event types
 * $events = odcm_get_log_event_types();
 * 
 * // Look up specific event
 * $order_completed = $events['order_completed'];
 * echo $order_completed['label']; // "Order Completed"
 * 
 * // Generate dynamic summary
 * $summary = sprintf(
 *     $order_completed['summary_template'],
 *     123 // order_id from context
 * );
 * echo $summary; // "Order #123 completed successfully"
 * 
 * // Check event category
 * if ($order_completed['category'] === 'core') {
 *     // This is a core event - always log
 * }
 * ```
 */
