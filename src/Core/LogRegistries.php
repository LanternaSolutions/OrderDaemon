 *
 * This function returns a comprehensive registry of all available log statuses
 * that can be used throughout the audit log system. Each status includes
 * display properties for consistent UI rendering and user experience.
 *
 * STATUS SYSTEM OVERVIEW:
 * ======================
 * 
 * The status system provides standardized categorization of log events
 * based on their outcome or severity level. This enables:
 * - Consistent visual representation across the admin interface
 * - Effective filtering and searching capabilities
 * - Clear communication of event importance to users
 * - Standardized CSS styling for status indicators
 * 
 * UI INTEGRATION:
 * ==============
 * 
 * Each status includes a CSS class that corresponds to styling in the
 * plugin's admin.css file. The classes follow the pattern:
 * odcm-status-pill-{status}
 * 
 * This enables consistent "pill" style status indicators throughout
