/**
 * Log System Registries - Central Hub for Event Types and Status Definitions
 *
 * This file contains the registry functions that serve as the single source of truth
 * for all known log event types and their display statuses in the Order Daemon
 * For Woocommerce plugin. It implements the Registry Pattern to provide structured,
 * extensible, and maintainable logging architecture.
 *
 * REGISTRY PATTERN OVERVIEW:
 * =========================
 * 
 * The registry pattern centralizes the definition of all log events and statuses,
 * providing a structured approach to logging that enables:
 * - Consistent event categorization (core, debug, premium, custom)
 * - Dynamic summary generation using sprintf templates
 * - Standardized status handling with UI styling
 * - Easy extension for new event types
 * - Clear separation between internal and external logging
 * 
 * LOG CATEGORIES:
 * ==============
 * 
 * - 'core': Standard, essential events generated by the plugin
 * - 'debug': Verbose, developer-focused events (only logged when ODCM_DEBUG is active)
 * - 'premium': Events triggered by premium features (always saved, marked for UI)
 * - 'custom': Default category for external/third-party events
 * 
 * INTEGRATION WITH EXISTING ARCHITECTURE:
 * ======================================
 * 
 * This registry follows the established patterns in the plugin:
 * - Similar structure to OptionRegistry for consistency
 * - Integration with existing entitlement system
 * - Compatibility with current asynchronous logging via Action Scheduler
 *
 * @package OrderDaemon\CompletionManager\Core
 * @since   1.0.0
 * @author  OrderDaemon Development Team
 * @link    https://docs.OrderDaemon.com/completion-manager/audit-log-system
 */

